<h3 class="faq">Frequently asked questions (F.A.Q.)</h3>

<p class="faq"><b>Where can I find the Manual for the use of Biotracking?</b><br />The most recent version of the Investigator’s manual can be accessed and downloaded from the menu on the left-hand side of the screen.</p>

<p class="faq"><b>How long will I have to wait for the central lab HER2 and hormonal receptor results?</b><br />Central results will be available for most of the cases not longer than 5 working days from reception at the central laboratory.<br /><br />
In cases with discordant results the timeline could go up to 7 working days (10-15% of all cases) when needed to double-check and repeat the assay, if necessary. 
Indeed, when running a central review, the central lab have to deal with tissues fixed in a very diverse manner, and to avoid false negative results sometimes it is needed to modify the analytical conditions to try and get a confirmatory evidence of the local data. This ensures that eligible patients are not denied enrollment in the trial because of pre-analytical oddities. As an example, if a case result HER2 negative centrally while it was locally considered 3+ by IHC and FISH amplified, they are reluctant to call it negative without having repeated the assays with different schedules of tissue pre-treatment, to accommodate different fixation procedures. The same holds true for ER and PgR status.</p>

<p class="faq"><b>How will I be informed of the central lab HER2 and hormonal receptors results?</b><br />The Central Lab results will be sent to the Principal Investigator by email. Results will also be available on the Biotracking (see Investigator’s manual).</p>

