<p class="faq"><b>I do not see my PDF form, what shall I do?</b><br/>You have to check , if you have one, the setting of your pop-up blocker. If it is turned on you will need to configure it and to allow pop-up windows from Biotracking website.</p>
<p class="faq"><b>What is a pop-up?</b><br/>A pop-up is a small web browser window that appears on top of the website you're viewing.</p>
<p class="faq"><b>What is a pop-up blocker?</b><br/>Pop-up Blocker is a feature integrate in modern web browser that lets you limit or block most pop-ups. You can choose the level of blocking you prefer, from blocking all pop-up windows to allowing the pop-ups that you want to see. When Pop-up Blocker is turned on, and a pop-up windows is blocked you will see usually a notification message. The notification message displayed depends on which browser you are using.</p>
<p class="faq"><b>Does Biotracking sytem uses pop-up?</b><br/>Yes that is why if your pop-up blocker is turned on you must configure it to accept pop-up from biotracking. Biotracking pop-ups are safe and do not contain any ad.</p>

