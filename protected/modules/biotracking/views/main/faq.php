<p class="faq"><b>Why should I update my web browser?</b><br/>Older browsers are more vulnerable to attacks than newer browsers. When Microsoft, Google, Mozilla or Apple find out about a security issue they fix it and make an updated browser available. In addition newer browsers use newer technology which means you get often a better browsing experience in terms of speed/performance 
