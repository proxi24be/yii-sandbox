<b>Aim</b><br/>
<br/>
To test whether the anti-HER2 therapy trastuzumab (Herceptin®) given for 1 or 2 years after standard chemotherapy improves disease-free and overall survival 
compared to observation in patients with over-expression of the HER-2 protein (HER-2 positive breast cancer). <br/>
<br/>
<br/>
<b>Added Value</b><br/>
<br/>
This landmark trial showed extraordinary results, with trastuzumab reducing by half the risk of relapse and by a third the risk of death from breast cancer in patients with HER-2 positive 
disease. Such positive results have not been reported for many years in oncology and have changed treatment practice worldwide for this patient population.<br/>
<br/>
<br/>
<b>Recruitment</b><br/>
<br/>
Recruitment started in December 2001 and was completed in June 2005. Drawing largely from the BIG network, the accrual rate exceeded all expectations, averaging over 200 patients per month. <br/>
<br/>
In total, 5.102 patients were recruited in 39 countries at 482 sites. Over 20 research groups participated in the trial, 17 affiliated with BIG, 7 non-affiliated and some independent sites.<br/>
<br/>
<br/>
<b>Outcomes</b><br/>
<br/>
1. The trial is in its follow-up phase.  However, with the first pre-planned interim analysis of the data showing such an improvement in disease-free survival with adjuvant trastuzumab after 1 year of 
median follow-up, the results were published in the New England Journal of Medicine (2005) and, together with the combined analysis of two North American trials, were presented at the 2005 ASCO Annual Meeting.<br/>
2. The second analysis at a median follow-up of 2 years showed an improvement in overall survival, confirming that  trastuzumab is extremely efficacious. The results were published in the Lancet (2007).<br/>
3. The comparison between patients treated with 1 year or 2 years of trastuzumab is pending, and these results are eagerly awaited.<br/>
4. Other publications have focused on other aspects of the study:<br/>
• Cardiac safety data – Journal of Clinical Oncology (2007 and 2010)<br/>
• Analyses of several subgroups showing the magnitude of benefit across all subgroups of patients – Annals of Oncology (2008)<br/>
• Degree of HER-2 gene amplification and its impact on disease-free survival – Journal of Clinical Oncology (2009)<br/>
<br/>
<br/>
<b>Tissue collection for translational research studies in association with the HERA trial (TransHERA)</b><br/>
<br/>
The objectives of TransHERA are to collect, store and analyse breast cancer tissue and serum from patients included in the HERA trial to allow the identification of molecular 
characteristics that are associated with the differential likelihood of response or that allow detection of residual or relapsing disease. The research proposals selected by the
 TransHERA Committee to use this tissue are expected to help identify  protein, genetic and molecular factors that might lead to new targets for the treatment of breast cancer or
 to a better understanding of the disease. This in turn can improve treatment strategies, enhance prevention and/or lead to cure.<br/>
