<b>Aim</b><br/>
<br/>
To compare disease-free survival in women with HER-2 over-expressing and/or amplified breast cancer randomised to lapatinib alone versus trastuzumab alone versus trastuzumab followed 
by lapatinib alone versus lapatinib in combination with trastuzumab.<br/>
<br/>
<br/>
<b>Added Value</b><br/>
<br/>
ALTTO is a global collaboration between BIG-affiliated research groups, the North America Breast Cancer Group (NABCG) and other groups neither affiliated with BIG nor NABCG. This trial 
is investigating whether lapatinib, an oral drug, can be as efficient as, or can improve the benefit of, adjuvant trastuzumab. <br/>
<br/>
The trial encompasses a substantial translational component, with central pathology review of the tumour tissue of all patients before randomisation to ensure 
that the right population is selected for treatment. Additionally, circulating tumour cell and proteomics research will be conducted with some patients to help 
better understand which patients will  likely benefit most from these anti-HER-2 therapies in the future.<br/>
<br/>
This trial has two pragmatic designs, which will broaden its application to actual clinical practice: <br/>
• Design 1: anti-HER-2 drugs are given after the completion of anthracycline-based chemotherapy<br/>
• Design 2 and 2B: anti-HER-2 drugs are given concomitantly with chemotherapy (taxanes in Design 2) or a non-anthracycline regimen (docetaxel plus carboplatin in Design 2B). <br/>
<br/>
<br/>
<b>Recruitment</b><br/>
<br/>
The trial, which opened in June 2007, is due to close in 2010. As with HERA and BIG 02-98, impressive accrual rates have been achieved, with over 350 patients recruited per month.<br/>
<br/>
In total, 34 groups involving over 1.300 hospitals in 44 countries around the world are taking part in this study, which will have recruited over 8.000 patients in just three years.<br/>
<br/>
