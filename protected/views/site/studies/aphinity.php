<p><b>Aim</b></p>
<p>To test whether dual blockade of HER2 with combination <?php echo utf8_decode($herceptin);?> and pertuzumab given in conjunction with standard cytotoxic 
chemotherapy improves invasive disease-free survival and overall survival compared to single blockade with <?php echo utf8_decode($herceptin);?> in patients with HER2 positive 
early breast cancer.</p>

<p><b>Expected added Value</b></p>
<p>The combination of <?php echo utf8_decode($herceptin);?> and pertuzumab has shown impressive results in patients with metastatic breast cancer, improving disease-free and overall survival<b><sup>(1)</sup></b>. 
    In the neoadjuvant setting, the HER2 dual blockade dramatically increased the rate of pathologic complete response in comparison to each drug alone<b><sup>(2)</sup></b>. Such impressive results are the rationale to test the combination of these two anti-HER2 drugs in the adjuvant setting. 
The results from the APHINITY trial could possibly improve cure rates for patients with HER2 positive early breast cancer and change clinical practice.
</p>

<p><b>Recruitment</b></p>
<p>Recruitment started in November 2011, the target sample size is 4800 patients. Around 600 centers from 43 countries will be actively enrolling patients. 
In October 2012 the number of patients enrolled by far exceeded the expected accrual rate.</p>

<p><b>Trial Ongoing</b></p>
<ol>
    <li>The trial is in the recruitment phase and is already open in most of the participating countries.</li>
    <li>This is a collaboration between academia (Breast International Group and its network) and pharma (Roche-Genentech).</li>
</ol>

<p><b>Tissue collection for translational research linked to the APHINITY trial</b></p>
<p>BrEAST has experience from previous trials in collecting and storing samples for translational research. 
The objectives of the APHINITY trial are to collect, store, and analyze breast cancer tissue and blood from patients included in the trial, to allow the identification of molecular characteristics that are associated with the differential likelihood of response, safety issues, 
or that allow detection of residual or relapsing disease. The translational research related to the APHINITY trial is expected to help identify protein, genetic, and molecular factors that might lead to new targets for the treatment of breast cancer or to a better understanding of the disease and safety profile. 
This in turn can improve treatment strategies, enhance prevention, and/or lead to cure.</p>
