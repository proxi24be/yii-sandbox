<b>Aim</b><br/>
<br/>
Following the same principles, an ALTTO “sister” trial is being conducted in parallel in collaboration with the BIG member group SOLTI 
(GRUPO ESPAÑOL DE ESTUDIO, TRATAMIENTO Y OTRAS ESTRATEGIAS EXPERIMENTALES EN TUMORES SÓLIDOS; <u><a href="http://www.gruposolti.org">http://www.gruposolti.org</a></u>): the Neo–Adjuvant 
Lapatinib and/or Trastuzumab Treatment Optimisation (NEO-ALTTO). This trial’s primary objective is to compare the rate of pathological complete response 
(total disappearance of the tumour) at the time of surgery between three treatment arms.<br/>
<br/>
<br/>
<b>Added Value</b><br/>
<br/>
This trial allows 3 drug therapies to be compared in various combinations to find the most effective treatment. Women with HER-2 positive tumours larger
 than 2 cm are randomly assigned to one of the following 3 treatment arms:<br/>
• lapatinib for 6 weeks followed by the addition of weekly paclitaxel for an additional 12 weeks<br/>
• trastuzumab for 6 weeks followed by the addition of weekly paclitaxel for an additional 12 weeks<br/>
• trastuzumab plus lapatinib for 6 weeks followed by the addition of  weekly paclitaxel for an additional 12 weeks. <br/>
<br/>
Definitive surgery is performed within 4 weeks of the last dose of paclitaxel. After surgery, patients receive 3 cycles of adjuvant chemotherapy (FEC: 5-fluorouracil, 
epirubicin and cyclophosphamide) followed by the same assigned pre-operative targeted therapy for 34 weeks, with the intention to complete 1 year of anti-HER-2 therapy.  <br/>
<br/>
<br/>
<b>Recruitment</b><br/>
<br/>
This trial started recruiting patients in early 2008 and completed in December 2009<br/>
<br/>
<br/>
<b>Outcomes</b><br/>
<br/>
The first trial results are expected in late 2010.<br/>
