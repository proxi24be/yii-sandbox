<b>Aim</b><br/>
<br/>
To compare docetaxel (Taxotere®) given sequentially to or concurrently with anthracycline-based chemotherapy versus anthracycline chemotherapy in patients with node-positive, early breast cancer.<br/>
<br/>
<br/>
<b>Recruitment</b><br/>
<br/>
The BIG 02-98 trial was BrEAST’s first large-scale study. Recruitment started in June 1998 and ended in 2001. In total, 2.887 patients were recruited in 21 countries across 172 sites. 
This trial demonstrated the power of BIG’s clinical network to dramatically increase the rate of patient recruitment.<br/>
<br/>
<br/>
<b>Eight research groups took part in the trial:</b><br/>
<br/>
• ABCSG – Austrian Breast & Colorectal Cancer Study Group<br/>
• ANZBCTG – Australian New Zealand Breast Cancer Trials Group<br/>
• DBCG  – Danish Breast Cancer Cooperative Group<br/>
• GEICAM – Grupo Español de Investigación en Cáncer de Mama (Spanish breast cancer group)<br/>
• GOCCHI – Grupo Oncológico Cooperativo Chileno de Investigatión (Chilean cancer group)<br/>
• IBCSG – International Breast Cancer Study Group <br/>
• ICORG  – All Ireland Co-operative Oncology Research Group<br/> 
• SBCG – Swedish Breast Cancer Group<br/>
<br/>
<br/>
<b>Outcomes</b><br/>
<br/>
1. The first results were presented at the 2006 ASCO (American Society of Clinical Oncology) Annual Meeting and showed an improvement in disease-free 
survival for docetaxel given sequentially to anthracycline chemotherapy (A-T-CMF versus A-CMF) chemotherapy at a median follow-up of 5 years. This 
improvement persisted at the 8-year median follow-up presented at the San Antonio Breast Cancer Symposium (SABCS) 2009.  An improvement in overall 
survival (ie, reduction of risk of dying because of breast cancer) has also been demonstrated with this sequential administration. <br/>
2. Two articles have also been published: <br/>
• On a sub-population of the study, in which  patients developed brain metastases  – Annals of Oncology (2008) <br/>
• On possible links between Body Mass Index/obesity and disease-free survival – Breast Cancer Research Treatment (2009)<br/>
<br/>
<br/>
<b>Current status</b><br/>
<br/>
The follow-up of patients is still ongoing. As foreseen in the protocol, an analysis is expected at the 10-year median follow-up (2012). 
Beyond providing answers to important scientific questions, another feature of this trial was the collection of tumour tissue from 1793 
patients who had consented to making such a donation for future breast cancer research. This allowed a study tumour repository to be created,
 which is governed by the TransTAX Committee. The role of this committee is to evaluate research proposals to analyse the tumour tissue using 
 different laboratory techniques and then to correlate those results with clinical outcome.<br/>
 <br/>
 TransTAX research is in line with today’s overall direction in cancer research, namely that a better understanding of  the biology of breast
 cancer through classic pathology and genomic research will ultimately result in treatments that are better tailored to the characteristics of 
 the cancer biology of individual patients. To date, 4 promising projects have been selected, and laboratory and statistical analyses are
 ongoing. The laboratories involved in theses analyses are located in Australia, France, Ireland and Belgium.<br/>
